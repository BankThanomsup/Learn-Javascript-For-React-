//push , pop ,shift , unshift

const data =[10,20,30]

data.push(500,1000,1500,...[2000,2500]) //spread operator
//console.log(data);

data.pop()  //ลบสมาชิกตัวสุดท้ายออกไป
console.log(data);

data.shift() //ลบมาชิกที่อยู่ข้างหน้าออกไป
console.log(data);

data.unshift(999)  //เพิ่มสามชิกตัวแรกเข้าไปในอาร์เรย์
console.log(data)